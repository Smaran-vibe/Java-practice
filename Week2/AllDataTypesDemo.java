class AllDataTypesDemo {
    public static void main(String[] args) {

        // 1. byte – 8-bit integer
        byte age = 21;

        // 2. short – 16-bit integer
        short year = 2026;

        // 3. int – 32-bit integer
        int salary = 50000;

        // 4. long – 64-bit integer
        long population = 8000000000L;

        // 5. float – 32-bit floating-point
        float temperature = 36.5f;

        // 6. double – 64-bit floating-point
        double distance = 12345.6789;

        // 7. char – single character
        char grade = 'A';

        // 8. boolean – true or false
        boolean isPassed = true;

        // Printing all values
        System.out.println("Age (byte): " + age);
        System.out.println("Year (short): " + year);
        System.out.println("Salary (int): " + salary);
        System.out.println("Population (long): " + population);
        System.out.println("Temperature (float): " + temperature);
        System.out.println("Distance (double): " + distance);
        System.out.println("Grade (char): " + grade);
        System.out.println("Passed (boolean): " + isPassed);
    }
}
